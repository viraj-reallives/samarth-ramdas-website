function getAuthorSubjectSlug(author) {
  return author.subjectSlug ?? author.slug
}

function authorMatchesSubjectEntry(author, subjectAuthor) {
  const authorSlug = getAuthorSubjectSlug(author)
  return (
    subjectAuthor.slug === authorSlug ||
    subjectAuthor.authorSlug === authorSlug ||
    subjectAuthor.slug === author.slug
  )
}

export function getAuthorBySlug(slug) {
  return authors.find((author) => author.slug === slug)
}

export function getAuthorUrl(slug) {
  return `/author/${slug}`
}

export function getSubjectsForAuthor(authorSlug) {
  const author = getAuthorBySlug(authorSlug)
  if (!author) return []

  const matchedSubjects = []

  for (const [subjectSlug, subjectAuthorList] of Object.entries(subjectAuthors)) {
    const hasAuthor = subjectAuthorList.some((subjectAuthor) =>
      authorMatchesSubjectEntry(author, subjectAuthor),
    )
    if (!hasAuthor) continue

    const subject = subjects.find((item) => item.slug === subjectSlug)
    if (subject) matchedSubjects.push(subject)
  }

  const order = authorSubjectOrder[authorSlug]
  if (!order) return matchedSubjects

  return [...matchedSubjects].sort(
    (a, b) => order.indexOf(a.slug) - order.indexOf(b.slug),
  )
}

export function getOtherAuthors(currentAuthorSlug) {
  const others = authors.filter((author) => author.slug !== currentAuthorSlug)
  const order = authorSidebarOrder[currentAuthorSlug]

  if (!order) return others

  return [...others].sort(
    (a, b) => order.indexOf(a.slug) - order.indexOf(b.slug),
  )
}

export function getAuthorSubjectUrl(subjectSlug, authorSlug) {
  const author = getAuthorBySlug(authorSlug)
  const subjectAuthorSlug = author ? getAuthorSubjectSlug(author) : authorSlug
  return `/subject/${subjectSlug}/author/${subjectAuthorSlug}`
}
